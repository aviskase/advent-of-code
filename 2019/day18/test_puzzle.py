import pytest

from .puzzle import bfs, Maze

a = '''#########
#b.A.@.a#
#########'''

b = '''########################
#f.D.E.e.C.b.A.@.a.B.c.#
######################.#
#d.....................#
########################'''

c = '''########################
#...............b.C.D.f#
#.######################
#.....@.a.B.c.d.A.e.F.g#
########################'''

d = '''#################
#i.G..c...e..H.p#
########.########
#j.A..b...f..D.o#
########@########
#k.E..a...g..B.n#
########.########
#l.F..d...h..C.m#
#################'''

e = '''########################
#@..............ac.GI.b#
###d#e#f################
###A#B#C################
###g#h#i################
########################'''


@pytest.mark.parametrize('maze, result', [
    (Maze(a.split()), 8),
    (Maze(b.split()), 86),
    (Maze(c.split()), 132),
    (Maze(d.split()), 136),
    (Maze(e.split()), 81),
])
def test_bfs(maze, result):
    assert bfs(maze) == result
